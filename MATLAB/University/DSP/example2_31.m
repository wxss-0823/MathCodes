%求系统的频率响应
%输入差分方程的系数
b=[0.0181,0.0543,0.0543,0.0181];
a=[1.0000,-1.7600,1.1829,-0.2781];
m=0:length(b)-1;l=0:length(a)-1;
K=500;k=0:1:K;
w=pi*k/K;
%计算频率响应
num=b*exp(-j*m'*w);
den=a*exp(-j*l'*w);
H=num./den;
magH=abs(H);angH=angle(H);
%画图
subplot(2,1,1);plot(w/pi,magH);title('幅度响应');grid;
xlabel('以\pi为单位的频率');ylabel('幅度');
subplot(2,1,2);plot(w/pi,angH);title('相位响应');grid;
xlabel('以\pi为单位的频率');ylabel('相位');